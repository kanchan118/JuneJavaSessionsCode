package javasessions;

public class ArithmeticConcepts {

	public static void main(String[] args) {
		
		System.out.println(10/2);
		System.out.println(9/2);
		System.out.println(9/2.0);//4.5
		System.out.println(9.0/2);//4.5
		System.out.println(8/3);//2
		System.out.println(9.0/2.0);
		
		//System.out.println(9/0);
		
		//System.out.println(9.0/0);//decimal # divided by 0 will give infinity
		
		System.out.println(0/9);
		System.out.println(9/0.0);//infinity; pure # by 0.0 
		
		//System.out.println(0/0);//Arithmetic exception
		
		System.out.println(0.0/0);//NaN - not a number
		System.out.println(0.0/0.0);//NaN
		
		System.out.println(0/0.0);//NaN
		
		System.out.println(9/9);
		
		System.out.println(9 % 3);//0
		
		System.out.println(9%2);//1
		
		System.out.println(17%3);
		
		//System.out.println(100/0 + "hello"); AE
		
		
	}

}
